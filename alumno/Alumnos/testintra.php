    function validacion() {
        const uls = document.querySelectorAll('form ul'); // Select all ul elements within the form

        for (const ul of uls) {
            const radios = ul.querySelectorAll('input[type="radio"]'); // Get all radio buttons within the ul

            let isSelected = false;
            for (const radio of radios) {
                if (radio.checked) {
                    isSelected = true;
                    break; // Exit the inner loop if a radio is selected
                }
            }

            if (!isSelected) {
                Swal.fire({
                    position: 'center',
                    type: 'warning',
                    title: 'Por favor, selecciona una opción en cada pregunta.',
                    showConfirmButton: true,
                    confirmButtonText: 'Aceptar',
                    timer: 20000
                });
                return false; // Prevent form submission if no radio is selected in an ul
            }
        }

        // If all uls have a selected radio button, return true to allow form submission
        return true;
    }
</script>
